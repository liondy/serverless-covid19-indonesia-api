async function getData() {
  let init = await fetch("https://data.covid19.go.id/public/api/skor.json");
  let response = await init.json();

  data.sort((a, b) => (a.id > b.id ? 1 : -1));
  response.data.sort((a, b) => (a.kode_kota > b.kode_kota ? 1 : -1));

  Object.keys(data).map(function (object) {
    data[object]["status"] = response.data[object]["hasil"];
    if (data[object]["status"] === "TIDAK ADA KASUS") {
      data[object]["nilai"] = 0;
    } else if (data[object]["status"] === "RESIKO RENDAH") {
      data[object]["nilai"] = 1;
    } else if (data[object]["status"] === "RESIKO SEDANG") {
      data[object]["nilai"] = 2;
    } else {
      data[object]["nilai"] = 3;
    }
  });
  return data;
}

